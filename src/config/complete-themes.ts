export function getThemeById(id: ThemeId): CompleteTheme | undefined {
  return completeThemes.find((theme) => theme.id === id);
}

export function getThemeTokens(
  themeId: ThemeId,
  mode: "light" | "dark" | "classic-dark"
) {
  const theme = getThemeById(themeId);
  if (!theme) return null;

  if (mode === "classic-dark") return theme.classicDark;
  return theme[mode];
}
